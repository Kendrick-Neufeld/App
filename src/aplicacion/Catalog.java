    private void BackButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BackButtonMouseClicked
        if (AccDevices) {
            if (CatalogPage != 0) {
                this.CatalogPage -= 6;
                DisplayDevices();
                int num = Integer.parseInt(PageNum.getText());
                num--;
                PageNum.setText("" + num);
            }
        } else {
            if (CatalogPage != 0) {
                this.CatalogPage -= 6;
                DisplayAccesorios();
                int num = Integer.parseInt(PageNum.getText());
                num--;
                PageNum.setText("" + num);
            }
        }
    }//GEN-LAST:event_BackButtonMouseClicked

